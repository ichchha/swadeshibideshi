package com.susankya.swadesibidhesi.models.user;

public class WishLineItem {
    public int wishlist_id;
    public int quantity;
    public String title;
    public int product;

    public int getWishlist_id() {
        return wishlist_id;
    }

    public int getQuantity() {
        return quantity;
    }

    public String getTitle() {
        return title;
    }

    public int getProduct() {
        return product;
    }
}
